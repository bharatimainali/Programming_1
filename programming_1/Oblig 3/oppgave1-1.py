# using the dictionaries about an animal

animal = {
    "species": "HoneyBadger",
    "name": "Nils",
    "sex": "Male"
}
# printing the animal name as a key_value
print(animal["name"])

# changing the sex of an animal by using the key_value
animal["sex"] = "female"

print(animal)

# adding the age of an animal.
animal["age"] = 84
print(animal)
